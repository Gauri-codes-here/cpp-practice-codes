#include<iostream>
using namespace std;
 int main()
 {
    int a = 88;
    int b = 99;
    int add = a+b;
    int sub = a-b;
    int mul = a*b;
    int div = a / b;
    int mod = a % b;
    cout<<"Addition "<< add << endl;
    cout<<"Subtraction "<< sub << endl;
    cout<<"Multiplication "<< mul << endl;
    cout<<"Division "<< div << endl;
    cout<<"Remainder "<< mod << endl;

    return 0;
 }